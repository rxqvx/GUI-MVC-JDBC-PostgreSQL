    private void alterarBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_alterarBtnActionPerformed
        // TODO add your handling code here:
        String nome = txtProd.getText();
        String id = txtId.getText();
        String newId = txtNewId.getText();

        Conexao conn = new Conexao();
        Produto produto = new Produto(nome, null, null, newId);
        Produto produto1 = new Produto(null, null, null, id);
        try {
            Connection c = conn.getConexao();
            ProdutoDAO dao = new ProdutoDAO(c);
            dao.Update(produto, produto1);
            JOptionPane.showMessageDialog(rootPane, "Id atualizado com sucesso!");
            System.out.println("Conectado ao banco e atualizado.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Falha!");
            System.out.println("Falha na conex√£o!");
        }
    }// GEN-LAST:event_alterarBtnActionPerformed

